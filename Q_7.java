package core2web.Control_Statements.Basic_IfElse_Assignment;

public class Q_7 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if(a > b){
            System.out.println("a is maximum between a and b");
        }else if(a < b){
            System.out.println("b is maximum between a and b");
        }else{
            System.out.println("Both are equal");
        }
    }
}
